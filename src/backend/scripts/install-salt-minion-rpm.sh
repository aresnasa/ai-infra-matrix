#!/bin/bash
# =============================================================================
# SaltStack Minion 安装脚本 (CentOS/RHEL/Rocky Linux)
# 从 AppHub 下载并安装 SaltStack Minion
# =============================================================================

set -e

# 参数检查
if [ $# -lt 3 ]; then
    echo "Usage: $0 <APPHUB_URL> <SALT_VERSION> <MASTER_HOST> [MINION_ID]"
    echo "Example: $0 http://192.168.0.200:53434 3007.8 192.168.18.154 my-minion-01"
    exit 1
fi

APPHUB_URL="$1"
SALT_VERSION="$2"
MASTER_HOST="$3"
MINION_ID="${4:-$(hostname)}"

echo "=========================================="
echo "Installing SaltStack Minion from AppHub"
echo "AppHub URL: ${APPHUB_URL}"
echo "SaltStack Version: ${SALT_VERSION}"
echo "Master Host: ${MASTER_HOST}"
echo "Minion ID: ${MINION_ID}"
echo "=========================================="

# 检测架构
ARCH=$(uname -m)
echo "Detected architecture: ${ARCH}"

# 设置AppHub包路径
APPHUB_BASE="${APPHUB_URL}/pkgs/saltstack-rpm"
VERSION="${SALT_VERSION}-0"

# 下载所需的SaltStack包
cd /tmp
echo "Downloading SaltStack packages from AppHub..."

# 下载 salt 基础包 (minion 的依赖)
echo "Downloading salt-${VERSION}.${ARCH}.rpm..."
curl -fsSL "${APPHUB_BASE}/salt-${VERSION}.${ARCH}.rpm" -o salt.rpm || {
    echo "Failed to download salt base package from AppHub"
    exit 1
}

# 下载salt-minion RPM
echo "Downloading salt-minion-${VERSION}.${ARCH}.rpm..."
curl -fsSL "${APPHUB_BASE}/salt-minion-${VERSION}.${ARCH}.rpm" -o salt-minion.rpm || {
    echo "Failed to download salt-minion from AppHub"
    exit 1
}

echo "Installing SaltStack packages..."
# 安装Python依赖
echo "Installing Python dependencies..."
sudo yum install -y python3 python3-pip 2>/dev/null || sudo dnf install -y python3 python3-pip || true

# 先安装 salt 基础包
echo "Installing salt base package..."
sudo yum install -y ./salt.rpm 2>/dev/null || sudo dnf install -y ./salt.rpm

# 再安装salt-minion
echo "Installing salt-minion..."
sudo yum install -y ./salt-minion.rpm 2>/dev/null || sudo dnf install -y ./salt-minion.rpm

# 清理临时文件
rm -f salt.rpm salt-minion.rpm

# 验证安装
echo "Verifying installation..."
salt-minion --version || {
    echo "Salt Minion installation verification failed"
    exit 1
}

# 配置 Salt Minion
echo "Configuring Salt Minion..."
sudo mkdir -p /etc/salt

# 创建 minion 配置文件
sudo tee /etc/salt/minion > /dev/null <<EOF
# SaltStack Minion Configuration
# Auto-generated by install-salt-minion-rpm.sh

master: ${MASTER_HOST}
id: ${MINION_ID}

# 自动接受 master 的 key
master_finger: ''

# 日志配置
log_level: info
log_file: /var/log/salt/minion

# 其他配置
retry_dns: 30
retry_dns_count: 3
acceptance_wait_time: 10
acceptance_wait_time_max: 60
EOF

echo "Configuration written to /etc/salt/minion"

# 启动并启用 Salt Minion 服务
echo "Starting Salt Minion service..."
sudo systemctl daemon-reload || true
sudo systemctl enable salt-minion || true
sudo systemctl restart salt-minion || true

# 等待服务启动
sleep 2

# 检查服务状态
if sudo systemctl is-active --quiet salt-minion; then
    echo "Salt Minion service is running"
else
    echo "Warning: Salt Minion service may not be running properly"
    sudo systemctl status salt-minion --no-pager || true
fi

echo "=========================================="
echo "SaltStack Minion installed successfully from AppHub"
echo "Version: $(salt-minion --version)"
echo "Master: ${MASTER_HOST}"
echo "Minion ID: ${MINION_ID}"
echo "Service Status: $(systemctl is-active salt-minion 2>/dev/null || echo 'unknown')"
echo ""
echo "Next steps:"
echo "1. On Salt Master, accept the minion key:"
echo "   salt-key -a ${MINION_ID}"
echo "2. Test connectivity:"
echo "   salt '${MINION_ID}' test.ping"
echo "=========================================="
